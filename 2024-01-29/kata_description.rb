# Kata: Sum of a Number Range
## Problem
Write a function that takes two integers, `start` and `end`, and returns the sum of all numbers between `start` and `end` (inclusive). If `start` is greater than `end`, the function should return 0.
  ### Example
  - Input: `start = 1`, `end = 5`
- Output: `15` (because 1 + 2 + 3 + 4 + 5 = 15)
## Considerations
  - Think about how you would handle the case where `start` is greater than `end`.
  - Consider using a loop or the mathematical formula for the sum of a series of numbers (arithmetic sum).